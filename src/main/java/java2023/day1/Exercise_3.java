package java2023.day1;
public class Exercise_3 {
    // This is the main method
    public static void main(String[] args) {

    	        // Initialize the counter
    	        int x = 2;
    	        double sum = 0.0; // Initialize sum as a double

    	        for (; x < 10; x++) {
    	            sum += x; // Add x to sum
    	        }

    	        System.out.println("Sum is: " + sum);
    	    

    }
}
